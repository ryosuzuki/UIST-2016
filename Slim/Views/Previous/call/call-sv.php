<h1 id="student-volunteers">Student Volunteers</h1>
<h3 id="deadline-august-10-2015-17-00-pdt">Deadline: August 10, 2015 17:00 PDT</h3>
<h3 id="application-instructions">Application Instructions</h3>
<p>To apply to be a student volunteer, please sign up at the <a href="http://chisv.org/uist2015/news">UIST SV portal</a>. </p>
<h3 id="questions-">Questions?  </h3>
<p>If you have any questions/problems, please contact the chairs at <a href="mailto:sv@uist.org">sv@uist.org</a>.</p>
<h3 id="benefits-and-duties">Benefits and Duties</h3>
<p>If you are a UIST Student Volunteer, you get:</p>
<ul>
<li>Free conference registration</li>
<li>Free T-shirt</li>
<li>A great SV party with free dinner </li>
<li>A chance to attend the premier forum for innovations in human-computer interfaces</li>
</ul>
<p>Student volunteers will be required to help set up and perform needed work during the conference. Duties include: bag stuffing, registration desk, AV assistance, poster and demo setup, badge checking, break monitoring, etc. Volunteers will also need to be available during the conference if tasks come up. SVs usually get to attend most of the sessions, even when working. There will be approximately 14 scheduled hours per SV. </p>
<h3 id="arriving-and-leaving">Arriving and Leaving</h3>
<p>SVs need to be available to help throughout the conference. You will need to arrive on or before Saturday afternoon (Nov. 7) for the SV orientation and should not leave the conference venue until sessions end on Wednesday (Nov. 11). Also, we strongly encourage you to put off leaving until as late as you possibly can, because it&#39;s traditional to have a small party for student volunteers on Wednesday evening. The time and place for this party will be announced when we know all the student volunteers&#39; travel plans.</p>
<h3 id="work-schedules">Work Schedules</h3>
<p>To give you an idea of what to expect, you can see the <a href="http://www.acm.org/uist/uist2008/uist06schedule.html">UIST 2006 Student Volunteer Schedule</a>.</p>
<h3 id="accommodation">Accommodation</h3>
<p>We will not be able to provide free accommodation, but we will help you team up with other student volunteers for sharing rooms and we will provide a list of hotels with affordable room rates. </p>
