<h2 id="appropriate-topics">Appropriate Topics</h2>
<p>Appropriate topics include but are not limited to:</p>
<ul>
<li>Significantly novel enabling technologies such as innovative input devices, displays, new interaction techniques, or new media that extend the boundaries of traditional interaction, such as natural user interfaces and interactions, augmented reality, mobile interaction, haptics and tactile feedback interfaces, ubiquitous computing (including wearables), social software, and computer-supported collaborative work.</li>
<li>Innovative user interfaces for difficult interaction contexts or challenging applications. Examples include managing large, complex information sets, usable privacy and security, multi-user interaction, crowdsourcing, fabrication, or techniques that span devices distributed in time and space</li>
<li>Breakthrough user experiences leveraging techniques such as machine learning, computer vision, computer graphics, speech processing, networking, or human perception and cognition</li>
<li>Innovative software architectures, design tools, toolkits, programming systems, development environments, tutorial and help systems that support the development and use of the above technologies in user interfaces.</li>
</ul>
