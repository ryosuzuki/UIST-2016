<h1 id="uist-2015-call-for-participation">UIST 2015 Call for Participation</h1>
<p>The ACM Symposium on User Interface Software and Technology (UIST) is the premier forum for innovations in human-computer interfaces. Sponsored by ACM special interest groups on computer-human interaction (SIGCHI) and computer graphics (SIGGRAPH), UIST brings together people from diverse areas including graphical &amp; web user interfaces, tangible &amp; ubiquitous computing, virtual &amp; augmented reality, multimedia, new input &amp; output devices, and CSCW.</p>
