			<h2 class="text-spaced">UIST 2015 Corporate Support</h2>

			<p>You are invited to support the UIST 2015 conference. This is an ideal way to demonstrate your organization's interest in and commitment to the field of user interface software and technology, and to publicize this support to many leaders and students in the field.</p>

			<p>Corporate support is typically used to permit a greater number of students to attend UIST at reduced fees without artificially increasing the general registration, and to allow student volunteers to receive complimentary registration. Corporate support can also be targeted to a particular event or activity at the UIST Conference.</p>

