				<a href="#platinum">Platinum</a>, <a href="#gold">Gold</a>, <a href="#silver">Silver</a>, and <a href="#bronze">Bronze</a>. We are also pleased to introduce this year the <a href="#friend">Friends of UIST</a> sponsorship level for individuals who wish to contribute to the conference.</p>

