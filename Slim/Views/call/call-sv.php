  </div>

  <h2>
    Benefits and Duties:
  </h2>
  <div class="text">
    If you are a UIST Student Volunteer, you get:
    <ul>
      <li>Free conference registration</li>
      <li>Free T-shirt</li>
      <li>A great SV party with free dinner</li>
      <li>A chance to attend the premier forum for innovations in human-computer
        interfaces</li>
    </ul>
      Student volunteers will be required to help set up and perform needed work
      during the conference. Duties include: bag stuffing, registration desk, AV
      assistance, poster and demo setup, badge checking, break monitoring, etc.
      Volunteers will also need to be available during the conference if tasks
      come up. SVs usually get to attend most of the sessions, even when working.
      There will be approximately 14 scheduled hours per SV.
  </div>
  <h3>Arriving and Leaving:</h3>
  <div class="text">
    SVs need to be available to help throughout the conference. You will
    need to arrive on or before Saturday afternoon (Oct. 15) for the SV
    orientation and should not leave the conference venue until sessions end on
    Wednesday (Oct. 19). Also, we strongly encourage you to put off leaving
    until as late as you possibly can, because it's traditional to have a small
    party for student volunteers on Wednesday evening. The time and place for
    this party will be announced when we know all the student volunteers' travel
    plans.
  </div>
  <h3>Work Schedules:</h3>
  <div class="text">
    To give you an idea of what to expect, you can see the <a href="http://www.acm.org/uist/uist2008/uist06schedule.html">UIST 2006 Student Volunteer Schedule</a>.
  </div>
  <h3>Accommodation:</h3>
  <div class="text">
    We will not be able to provide free accommodation, but we will help you
    team up with other student volunteers for sharing rooms and we will provide
    a list of hotels with affordable room rates.
  </div>
