Student Volunteers (Deadline: July 25, 2016 17:00 PDT)

Application Instructions:

To apply to be a student volunteer, please sign up at the UIST SV portal.

Questions?

If you have any questions/problems, please contact the chairs at sv@uist.org.

Benefits and Duties:

If you are a UIST Student Volunteer, you get:

 Free conference registration

 Free T-shirt

 A great SV party with free dinner

 A chance to attend the premier forum for innovations in human-computer interfaces

Student volunteers will be required to help set up and perform needed work during the

conference. Duties include: bag stuffing, registration desk, AV assistance, poster and demo

setup, badge checking, break monitoring, etc. Volunteers will also need to be available during

the conference if tasks come up. SVs usually get to attend most of the sessions, even when

working. There will be approximately 14 scheduled hours per SV.

Arriving and Leaving: SVs need to be available to help throughout the conference. You will

need to arrive on or before Saturday afternoon (Oct. 15) for the SV orientation and should not

leave the conference venue until sessions end on Wednesday (Oct. 19). Also, we strongly

encourage you to put off leaving until as late as you possibly can, because it's traditional to have

a small party for student volunteers on Wednesday evening. The time and place for this party

will be announced when we know all the student volunteers' travel plans.

Work Schedules: To give you an idea of what to expect, you can see the UIST 2006 Student

Volunteer Schedule.

Accommodation: We will not be able to provide free accommodation, but we will help you

team up with other student volunteers for sharing rooms and we will provide a list of hotels with

affordable room rates.

Relevant Dates:

 July 25, 2016: Application Deadline (by 17:00 PDT)

 July 31, 2016: Application Notified (by 17:00 PDT)

Student Volunteer Chairs: (sv@uist.org)

David Lindlbauer (TU Berlin, Germany)

Jun Kato (National Institute of Advanced Industrial Science and Technology, Japan)










