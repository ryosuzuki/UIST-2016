        <h1>UIST 2016 Video Previews</h1>
        <div class="text">
          <b>Video Previews</b> are 30-second videos that can be thought of as an elevator pitch to give a glimpse of your work, and are here to help connect your work with the community at the conference and beyond. Since their introduction at CHI 2013, Video Previews have been adopted in world first class conferences such as ACM UIST and IEEE VIS. They provide the authors with a great opportunity to publicize their work to a wider audience and to attract more readers to their paper (*).
          <br>
          <br>
          UIST 2016 will continue the use of video previews. All authors of accepted Papers and Notes are <b>required</b> to submit a 30 seconds video that describes their work. The Video Previews will be made available through different mediums before and during the conference to help attendees plan their daily schedules (ACM YouTube Channel, conference program application, digital proceedings). They will also be embedded on the abstract page of the ACM Digital Library.
          <br>
          <br>
          If you have any questions, please contact the video preview chairs at <a href="mailto:videopreviews@uist.org">videopreviews@uist.org</a>.
          <br>
          (*) UIST 2014 Video Previews have a record of 150,000 views since Oct. 2014, and CHI 2015 Video Previews have about 180,000 views since April 2015 (aggregated data from the ACM YouTube Channel).
        </div>

        <h2 id="deadline">
          Deadline
        </h2>
        <div class="text">
          Please carefully read the following instructions and submit your Video Preview in the dedicated &quot;Video Preview&quot; field of your final submission at <a href="videopreviews@uist.org">http://www.precisionconference.com/~sigchi</a> by <b>August 10th 2016, 17:00 PDT</b>.
          <br>
          <br>
          Video previews are mandatory for the following venues:
          <ul>
            <li>Papers and Notes</li>
          </ul>
          Some of the Video Previews will be used for the conference teaser. The <b>teaser/program video preview will be released on Oct. 2nd</b> (two weeks before the conference) and the <b>Video Previews will be released on Oct. 9th</b> (one week prior to the conference). If you <b>do not want your material to be part of the teaser or to be released prior to the conference, uncheck the ‘Early Release’ checkbox</b> on your final submission form (your video will then be released the day before the conference).
        </div>

        <h2 id="instructions">
          Instructions
        </h2>
        <div class="text">
          <ul>
            <li>The first <b>3 seconds</b> of the video must include the <b>title of your paper, authors list, affiliations and contact information</b>.</li>
            <li>The rest of the video should communicate <b>the topic and the message</b> of your work (like an elevator pitch)</li>
            <li><b>The video must be appropriate</b> in terms of rights, use of language, viewpoints or imagery, and should not offend any individuals or groups at the conference or elsewhere (we reserve the right to reject videos whose content is inappropriate).</li>
            <li><b>Subtitles</b> should be included to improve accessibility to your video (recommended)</li>
            <li><em>No advertisement for the conference talk</em>. Video Previews will be archived on the abstract page of the ACM Digital Library as a short video figure to overview your work. Their diffusion therefore goes beyond the conference and should not be seen as a teaser to the presentation. Instead, Video Previews should be thought of as a video abstract to attract more readers to the paper.</li>
          </ul>
          <p>Your Video Preview material must conform to the format as described in the submission guide below. We strongly recommend you to read the <a href="http://uist.acm.org/uist2016/video-previews-detail">detailed guidelines</a>, which list a series of technical as well as production recommendations to help you create a compelling video preview of quality. For inspiration, you can watch the Video Previews of <a href="https://www.youtube.com/playlist?list=PLn0nrSd4xjjaQqj7IlilssxHt9xaSG039">UIST 2014</a> and <a href="https://www.youtube.com/playlist?list=PLn0nrSd4xjjZp2cUxcrwGhw8hEySRi-bn">CHI 2015</a>.</p>
        </div>

        <h2 id="submission-guide">
          Submission guide
        </h2>
        <div class="text">
          <p>The following checklists have been designed to help you make sure that your material conforms to the Video Previews requirements. Please make sure that you go through <b>every</b> item of these lists. Detailed guidelines and advices are available by following the (i) icons.</p>
          <p>Before submitting, we also invite you to check the compliance of your file using the <a href="http://videochecker.afihm.org/">Video Previews Checker</a></p>
        </div>

        <h2 id="technical-requirements">
          Technical requirements
        </h2>
        <div class="text">
          <ul>
            <li>video file size must not exceed <b>20MB</b></li>
            <li>video file must use the <b>MPEG-4 format (.mp4) with the H.264 codec</b>  <a href="video-previews-detail#format"><i class="fa fa-question-circle"></i></a></li>
            <li>video duration must not exceed <b>30 seconds.</b></li>
            <li>video resolution must be at <b>720p (1280x720).</b></li>
          </ul>
        </div>

        <h2 id="content-requirements">
          Content Requirements
        </h2>
        <div class="text">
          <ul>
            <li>video must include an <b>opening title</b> <a href="video-previews-detail#opening-title"><i class="fa fa-question-circle"></i></a></li>
            <li>video should be <b>captioned</b> (recommended)  <a href="video-previews-detail#subtitles-and-captions"><i class="fa fa-question-circle"></i></a></li>
            <li>permission obtained for all <b>copyright and 3rd party material</b>. <a href="video-previews-detail#copyright-sanity-check"><i class="fa fa-question-circle"></i></a></li>
          </ul>
