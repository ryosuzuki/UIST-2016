    <h2>Doctoral Symposium (Deadline: July 7, 2016 12:00 PDT)</h2>
    <div class="text">
      The UIST Doctoral Symposium is a forum in which Ph.D. students can meet and discuss their work with each other and a panel of experienced UIST researchers in an informal and interactive setting. We welcome applications from current Ph.D. students studying within the full range of disciplines and approaches that contribute to the UIST community. We will give preference to applicants beyond the proposal stage and well into their dissertation research.
      <br>
      <br>
      Each applicant should provide a short written paper (no more than four pages in the official SIGCHI conference publication format(

      <a href="http://www.sigchi.org/publications/chipubform/sigchi-papers-word-template/view" target="_blank">Word Template</a>

       or

      <a href="http://www.sigchi.org/publications/chipubform/sigchi-papers-latex-template/view" target="_blank">Latex Template</a>

      ), as described in

      <a href="https://uist.acm.org/uist2016/author-guide" target="_blank">the author's guide</a>

      ). Note that doctoral symposium submission, unlike paper submission, is not anonymous. This paper should describe ongoing work and might summarize the student's full dissertation work, or highlight a particular part in depth.

      <br>
      <br>

      The Doctoral Symposium committee will select approximately eight students to participate. Each student will be expected to give a short presentation of their work, which will be followed by extensive discussion with the panel and the other student participants. The symposium will be held on <b>October 16th</b>. In addition, each student is encouraged to present a poster describing his or her work to the full conference; posters will be on display for two days during the conference.Participants will be selected based on their anticipated contributions to the breadth and depth of the intellectual discussions of the symposium.

      <br>
      <br>

      The symposium will start with an informal dinner on October 15th, continue all the following day, and conclude with poster presentations. Doctoral Symposium abstracts will be published together with poster and demonstration abstracts and made available digitally to conference attendees. Posters may also be published at the participant's discretion. We cannot make a promise at this point, but UIST conference would like to pay the participants for some part of their registration and hotel costs. We would like to support the flight costs as well, but we need to ask for your kind understanding that it might be difficult to cover all of the costs. Doctoral Symposium papers and poster sketches should be submitted electronically to Precision Conference.
    </div>
